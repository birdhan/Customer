package com.fmcg.pojo;

public class Ktype {
	private String id;
	private String ktype;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getKtype() {
		return ktype;
	}
	public void setKtype(String ktype) {
		this.ktype = ktype;
	}
	@Override
	public String toString() {
		return "Ktype [id=" + id + ", ktype=" + ktype + "]";
	}
	
	
	
}
