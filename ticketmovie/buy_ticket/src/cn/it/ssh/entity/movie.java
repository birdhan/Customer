package cn.it.ssh.entity;

public class movie {

	private int id;
	private String movie_name;
	private String movie_type;
	private String movie_img;
	private double movie_price;
	private String movie_sketch;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMovie_name() {
		return movie_name;
	}
	public void setMovie_name(String movie_name) {
		this.movie_name = movie_name;
	}
	public String getMovie_type() {
		return movie_type;
	}
	public void setMovie_type(String movie_type) {
		this.movie_type = movie_type;
	}
	public String getMovie_img() {
		return movie_img;
	}
	public void setMovie_img(String movie_img) {
		this.movie_img = movie_img;
	}
	public double getMovie_price() {
		return movie_price;
	}
	public void setMovie_price(double movie_price) {
		this.movie_price = movie_price;
	}
	public String getMovie_sketch() {
		return movie_sketch;
	}
	public void setMovie_sketch(String movie_sketch) {
		this.movie_sketch = movie_sketch;
	}
	
}
